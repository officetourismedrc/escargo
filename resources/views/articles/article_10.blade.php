                        <p>Au bord du Lac Lubumbashi qui borde l'hôtel Karavia où ils ont pris leurs quartiers, les stands rivalisent d'ardeur pour se faire remarquer des visiteurs. Des visiteurs venus pour la plupart des contrées lointaines.</p>
