                    <p>Cette date a été choisie pour commémorer en rappel de l’adoption de la Charte de la renaissance culturelle africaine, adoptée en 1986 à Khartoum, au Soudan. 33 ans après, soit en 2019, à l'initiative du gouvernement du Togo et de l’ONG RAPEC et de son Président Ayite John Dossavi, les 193 États membres de l'UNESCO ont institutionnalisé la Journée Mondiale de la Culture Africaine et Afrodescendante (JMCA). </p>
                    <p>Deux jours avant la célébration de cette édition 2025, il y a eu l'annonce du tandem « Brazzaville-Kinshasa comme Capitales Africaines de la Culture pour les années 2025-2026. » Le programme des Capitales Africaines de la Culture vise à structurer les écosystèmes culturels africains, à promouvoir leur autonomie économique et à renforcer le dialogue entre les villes et territoires, les acteurs et professionnels des arts, de la culture et des activités créatives pour faire de la culture le quatrième pilier du développement durable aux cotés des piliers économique, social et environnemental. </p>
                    <p>Ses Capitales Africaines de la Culture seront, le temps de leur mandat, des plateformes internationales de célébration de l’authenticité et de la diversité culturelle africaine. La ville désignée Capitale Africaine de la Culture est pendant deux années la vitrine de l’excellence culturelle et créative de l’Afrique et de sa Diaspora et le lieu de promotion de son rayonnement en Afrique et de par le monde. Une célébration de l’Afrique créative et résiliente.</p>
                    <p>Rabat, la Capitale du Royaume du Maroc, a été la toute première Capitale Africaine de la Culture pour les années 2022/2023. Près de 100 événements culturels de grande portée ont été organisées au cours de cette célébration qui a connu un succès éclatant. </p>
                    <p>La célébration de Brazzaville et Kinshasa comme Capitale Africaine de la Culture 2025-2026 marque une nouvelle étape dans la promotion de la richesse culturelle et créative de l’Afrique. Brazzaville, connue pour son patrimoine artistique et musical emblématique, et Kinshasa, vibrante métropole et foyer d’innovation culturelle, porteront fièrement l’étendard de ce programme au cœur de l’Afrique centrale.</p>
