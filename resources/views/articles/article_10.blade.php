                      <p>Parmi les stands dressés ici, il y a celui de l'Office National du Tourisme. En sa qualité d'établissement public dédié à la promotion et le développement du tourisme en République Démocratique du Congo, l'ONT n'a pas loupé cette occasion de la 5e édition de la SCT. Madame le Directeur Général de l'ONT, Jolie Yombo Mukendi a mis toutes les batteries en marche pour avoir un stand à la dimension de l'événement. L'ONT s'est transporté ici pour se faire connaître du grand public d'une part, d'autre part vendre la destination RDC dans ses différentes facettes.</p>
                      <p>Installée au stand de l'ONT, madame le Directeur Général, Jolie Yombo Mukendi en a profité pour faire connaissance de son  personnel basé dans le Haut-Katanga, au sud-est de la République Démocratique du Congo. Une poignée de main chaleureuse empreinte de sourire, la numéro de l'ONT s'est entretenu pendant quelques minutes avec ses hommes et femmes, à tour de rôle.</p>
                      <p>Autres stands au bord du Lac Lubumbashi sont ceux des  provinces. Chacune d'elles a étalé ses vertus touristiques, gastronomiques, culturelles et autres.
Quant aux provinces minières, elles ont exposé des morceaux des minerais dont elles regorgent. Leurs sites touristiques ne sont pas oubliés. </p>
                      <p>Les opérateurs touristiques ne sont pas en reste. Les restaurateurs, les hôteliers, les compagnies aériennes, les agences de voyage et tant d'autres opérateurs sont également de la partie. </p>
                      <p>Les visiteurs, les touristes et autres curieux arpentent les allées de la 5e édition de la Semaine Congolaise du Tourisme arrosés par la fraîcheur du Lac Allilac. Au pays de la rumba, les rythmes musicaux ne sont pas en reste.</p>
