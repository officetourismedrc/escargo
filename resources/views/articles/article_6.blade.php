                    <p>Commémoration ce vendredi 24 janvier 2025 de la Journée Mondiale de la Culture Africaine et Afrodescendante, JMCA en sigle, sous le thème : “Préserver et promouvoir les héritages ancestraux : un élan de fraternité intergénérationnelle”. </p>
