                    <p>Après un échange en tête-à-tête, les deux personnalités ont élargi la rencontre à leurs collaborateurs respectifs. Le Gouvernement provincial de Kinshasa via son ministère du Tourisme et l’ONT s’alignent pour la construction et la mise en service des « Maisons du tourisme » ; Elles seront au total 24 sur la ville, en raison d’une « Maison du Tourisme » pour chaque commune de ville capitale. </p>
                    
                    <p>Ces « Maisons du Tourisme » serviront à valoriser et promouvoir le potentiel touristique local des communes, en même temps qu’elles constitueront les principaux centres d’accueil, d’information et d’orientation touristique. </p>
                    <p>En outre, le Ministère provincial en charge du Tourisme et l’ONT renforcent leur coopération à travers quelques projets importants, à savoir : - La matérialisation du projet de construction des Aires de repos et portiques électroniques, aux points d’entrée terrestres de Kinshasa : Mintendi et Mongata. - ⁠L’enrichissement du Guide Touristique Particulier de Kinshasa ; « Les Inconditionnels » pour en faire le support d’accompagnement pour les touristes à Kinshasa. - ⁠L’accompagnement de l’ONT dans l’effectivité du programme du Tourisme scolaire dont le lancement interviendra ce 30 Avril, en marge de la Journée nationale de l’enseignement. </p>
                    <p>Aligné à la vision de S.E.M. Daniel BUMBA LUBAKI, Gouverneur de la ville, le ministre provincial en charge du Tourisme a réitéré la disponibilité de son ministère a accompagner et faciliter le travail de l’Office National du Tourisme dans la ville de Kinshasa. </p>
                    
