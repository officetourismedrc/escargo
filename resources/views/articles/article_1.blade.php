                <div class="actualite-text-text">
                    <p>L'Office National du Tourisme (ONT) et l'Agence Nationale pour la Promotion des Investissements (ANAPI) ont signé ce mardi 11 mars 2025 à Kinshasa, un protocole d'accord pour la promotion du tourisme en RDC. Cet accord de partenariat a été signé au siège de l'ANAPI par mesdames les Directeurs Généraux de l'ONT, YOMBO MUKENDI Jolie et PUNGU LUAMBA Rachel de l'ANAPI. </p>
                    <p>Avant la signature proprement dit de ce protocole d'accord, les dirigeantes de ces deux établissements publics ont tour à tour pris la parole pour placer les choses dans leur contexte. </p>
                    <p>La première à s'exprimer, madame le Directeur Général de l'Office National du Tourisme a rappelé que ce n'est pas la première fois que l'ONT et l'ANAPI travaillent ensemble. Ce protocole d'accord est plutôt l'expression d'ambitions plus grandes de ces deux structures étatiques pour la promotion du tourisme en RDC. </p>

                    <p>"Nous avons eu à effectuer quelques exercices sur terrain auparavant. Etant donné que notre vision et nos perspectives d'avenir s'élargissent. Et d'un commun accord avec madame le DG nous avons jugé judicieux de pouvoir les circonscrire dans un cadre légal bien défini, cela pour nous permettre d'atteindre efficacement nos objectifs communément fixés mais cette fois-ci dans un cadre de mutualisation d'efforts pour mieux et davantage promouvoir le tourisme congolais", a dit madame le Directeur Général de l'ONT,YOMBO MUKENDI Jolie. </p>
                    <p><img style="border-radius:1rem;" src="{{asset('/images/articles-images/cr2WVqEwZIdDjXI5eCeYTv7tYAM4r3v5HwwpRKCW.webp')}}" alt="dg ont and dg anapi"></p>
                    <p>A son tour, madame le Directeur Général de l'ANAPI a loué le potentiel touristique de la RDC qui reste à développer. Et, pour mettre en valeur ce potentiel touristique inouïe dont dispose la République Démocratique du Congo, il y a des défis majeurs sur lesquels il faut travailler pour que le tourisme devienne réellement un secteur attractif en RDC. </p>
                    <p>Il faut pour cela actualiser les textes réglementaires, le respect des normes internationales, la difficulté d'accès à certains sites touristiques et la persistance des poches d'insécurité dans certaines parties du pays notamment dans l'Est où se trouve le parc de Virunga. Il y a aussi le besoin de renforcement des capacités à tous les niveaux de ce secteur. </p>
                    <p>C'est justement l'existence de tous ces défis qui a justifié la signature de ce protocole d'accord entre les deux institutions. </p>
