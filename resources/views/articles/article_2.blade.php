                    <p>Lors de la 69ᵉ session de la Commission de la Condition de la Femme (CSW) aux Nations Unies à New York , Madame Malicka Mukubu, Présidente du Conseil d'Administration de l'Office National du Tourisme (ONT) , a pris la parole pour rappeler le rôle fondamental des femmes dans la construction de sociétés pacifiques et inclusives. </p>
                </div>
                <div class="actualite-text-text">
                    <p>Dans son intervention sur le thème *"Avancement des femmes : Prérequis pour une société pacifique"*, elle a souligné que, depuis toujours, la femme est un pilier de la stabilité sociale. </p>
                    <p>Avant la signature proprement dit de ce protocole d'accord, les dirigeantes de ces deux établissements publics ont tour à tour pris la parole pour placer les choses dans leur contexte. </p>
                    <p>Son influence s'étend bien au-delà du cadre familial et s'inscrit dans les dynamiques de développement, de paix et de cohésion nationale. Pourtant, malgré son rôle central, elle reste souvent la première victime des inégalités, de la discrimination et des violences. </p>

                    <p>Évoquant l'évolution du combat pour les droits des femmes, elle a rappelé que la première *Conférence mondiale sur le statut des femmes en 1975* à Mexique avait déjà alerté la communauté internationale sur les discriminations persistantes à l'égard des femmes. </p>
                    <p>En 1995, la Déclaration de Pékin a constitué un tournant majeur en définissant des principes clairs pour garantir l'égalité des sexes et renforcer l'autonomie des femmes. </p>
                    <p>Trente ans plus tard, le bilan reste contrasté et soulève une question essentielle : les sociétés offrent-elles aujourd'hui aux femmes les moyens d'exercer pleinement leur rôle ? </p>
                    <p>L'éducation et l'indépendance économique ont été mises en avant comme des leviers essentiels pour renforcer la participation des femmes au développement. </p>
                    <p>Selon Madame Malicka Mukubu, permettre aux femmes d'accéder à une éducation de qualité et à des opportunités économiques équitables est une condition incontournable pour bâtir une société stable et prospère. </p>
                    <p>Elle a également insisté sur la nécessité d'une plus grande inclusion des femmes dans les instances décisionnelles, afin qu'elles puissent contribuer efficacement aux politiques de paix et de développement. </p>
                    <p>Dans un contexte où les défis restent nombreux, elle a rappelé l'engagement de la [Nation, par ex. RDC] en faveur de la promotion du rôle des femmes dans le processus de cohésion sociale et de consolidation de la paix. </p>
                    <p>En conclusion, elle a affirmé avec conviction que les temps changent, les sociétés évoluent, mais la femme demeure un acteur inébranlable du progrès et de la stabilité . </p>
